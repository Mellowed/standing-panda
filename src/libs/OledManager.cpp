void OledManager::goingUp(float height_cm, float roll, String msg)
{
   _display.invertDisplay(false);
   _display.clearDisplay();

   String heading = String(height_cm, 1) + "cm";
   printText(heading.c_str(), 2, 5, 2);

   printText(msg.c_str(), 2, 40, 1);

   String verbose = String(height_cm, 0) + "cm | " + String(roll, 1) + "d";
   printText(verbose.c_str(), 2, 56, 1);

   _display.drawLine(0, 52, 85, 52, WHITE);
   _display.drawLine(85, 52, 85, 64, WHITE);
   _display.fillRect(107, 25, 6, 45, WHITE);
   _display.fillTriangle(110, 2, 100, 25, 120, 25, WHITE);
   _display.display();
}

void OledManager::goingDown(float height_cm, float roll, String msg)
{

   _display.invertDisplay(false);
   _display.clearDisplay();

   String heading = String(height_cm, 1) + "cm";
   printText(heading.c_str(), 2, 5, 2);

   printText(msg.c_str(), 2, 40, 1);

   String verbose = String(height_cm, 0) + "cm | " + String(roll, 1) + "d";
   printText(verbose.c_str(), 2, 56, 1);

   _display.drawLine(0, 52, 85, 52, WHITE);
   _display.drawLine(85, 52, 85, 64, WHITE);
   _display.fillRect(107, 0, 6, 45, WHITE);
   _display.fillTriangle(110, 64, 100, 45, 120, 45, WHITE);
   _display.display();
}

void OledManager::printText(const char *msg, int x, int y, int size)
{
   _display.setTextSize(size);
   _display.setTextColor(WHITE);
   _display.setTextWrap(false);
   _display.setCursor(x, y);
   _display.println(msg);
   _display.display();
}